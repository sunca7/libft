#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	c;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	c = '\n';
	write(1, &c, 1);
}
