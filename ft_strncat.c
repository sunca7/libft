#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		a;
	size_t	b;

	a = 0;
	while (s1[a])
		a++;
	b = 0;
	while (s2[b] && b < n)
	{
		s1[a] = s2[b];
		a++;
		b++;
	}
	s1[a] = '\0';
	return (s1);
}
